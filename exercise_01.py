# Declare variables with different data types

# Integer variables (case-sensitive)
count = 10
Count = 15
total_count = 20

# Float variable
decimal_value = 3.14

# String variable
greeting_message = "Hello, Python!"

# Boolean variable
is_active = True

# None value
nothing = None

# Print the output in the required format
print(f"Integer (count): {count}")
print(f"Integer (Count): {Count}")
print(f"Integer (total_count): {total_count}")
print(f"Decimal: {decimal_value}")
print(f"Text: {greeting_message}")
print(f"Boolean: {is_active}")
print(f"None Value: {nothing}")
